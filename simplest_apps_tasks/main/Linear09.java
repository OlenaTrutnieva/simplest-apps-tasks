package by.training.simplest_apps_tasks.main;

public class Linear09 {

	public static void main(String[] args) {
		double a;
		double b;
		double c;
		double d;
		double y;
		
		a = 10;
		b = 0.750;
		c = -15;
		d = -2;
		
		y = ((a / c) * (b / d)) - ((a * b - c) / (c * d));
		
		System.out.println("y =" +y);
		
	}

}
